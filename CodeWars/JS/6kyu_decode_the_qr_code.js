function scanner(qrcode) {
    let bitString = "";
    const maskO = (x,y,j)=>qrcode[y][x-j]^((x-j)+y)%2 === 0
    var row = 0;
    for (let x = 20; x > 8; x -= 2) {
        row++;
        if (row % 2 === 0) {
            for (let y = 9; y < 21; y++) {
                for (let j = 0; j < 2; j++) {
                    bitString+=maskO(x,y,j)
                }
            }
        } else {
            for (let y = 20; y > 8; y--) {
                for (let j = 0; j < 2; j++) {
                    bitString+=maskO(x,y,j)
                }
            }
        }
    }
    const words = parseInt(bitString.slice(4, 12), 2);
    let start = 12,
        end,
        result = "";
    for (let i = 0; i < words; i++) {
        end = start + 8;
        result += String.fromCharCode(parseInt(bitString.slice(start, end), 2));
        start = end;
        end = end + 8;
    }
    return result;
}

